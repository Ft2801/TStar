/*
 * WCSMosaicDialog.cpp
 *
 * Implementation of WCSMosaicDialog.
 *
 * Build overview
 * --------------
 * 1. The user selects two or more images from the list and chooses one as the
 *    reference anchor.
 * 2. onApply() calls buildMosaic(), which:
 *    a. Projects the four corners of every selected panel through the WCS of
 *       that panel into sky coordinates, then back into the pixel frame of
 *       the anchor. This establishes the bounding box of the full mosaic in
 *       anchor-pixel space.
 *    b. Allocates an output buffer sized to that bounding box and adjusts the
 *       anchor WCS (CRPIX) to account for the offset so the output carries a
 *       valid WCS solution.
 *    c. For every output pixel, converts anchor-pixel -> sky -> source-panel
 *       pixel using WCSUtils, samples the source with bilinear interpolation,
 *       and accumulates the weighted contribution into the output buffer. The
 *       per-pixel weight comes from a feathered blend mask generated by
 *       Stacking::Blending::generateBlendMask(). OpenMP is used to parallelise
 *       the per-panel reprojection loops.
 *    d. Divides each output pixel by the accumulated weight sum (normalisation)
 *       and returns the finished buffer.
 * 3. If buildMosaic() succeeds, mosaicReady() is emitted so the main window
 *    can open the result in a new viewer.
 */

